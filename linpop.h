#ifndef __LINPOP_H
#define __LINPOP_H

#define LINPOP_VERSION                 0x001
#define LINPOP_PORT                    0x979	//port 2425
//#define LINPOP_BR_IP		255.255.255.255


#define LINPOP_NOOPERATION	0x00000000UL	//不进行任何操作

#define LINPOP_BR_ENTRY 	0x00000001UL	//用户上线(以广播方式登陆)
#define LINPOP_BR_EXIT 		0x00000002UL	//用户退出(以广播方式退出)
#define LINPOP_ANSENTRY 	0x00000003UL	//应答用户上线
#define LINPOP_BR_ABSENCE 	0x00000004UL	//改为缺席模式

#define LINPOP_BR_ISGETLIST     0x00000010UL
#define LINPOP_OKGETLIST        0x00000011UL
#define LINPOP_GETLIST          0x00000012UL
#define LINPOP_ANSLIST          0x00000013UL
#define LINPOP_BR_ISGETLIST2    0x00000018UL

#define LINPOP_SENDMSG 		0x00000020UL	//发送消息
#define LINPOP_RECVMSG 		0x00000021UL	//消息接受验证

#define LINPOP_READMSG 		0x00000030UL	//消息打开通知
#define LINPOP_DELMSG 		0x00000031UL	//消息丢弃通知
#define LINPOP_ANSREADMSG	0x00000032UL
#define LINPOP_GETFILEDATA 	0x00000060UL	//同意传输文件
#define LINPOP_SENDFILEREQUEST 	0x00000061UL	//请求发送文件
#define LINPOP_RELEASEFILES 	0x00000062UL	//停止接收文件
#define LINPOP_CHANGE_IMAGE 	0x00000063UL	//更改用户形象

/*  option for all command  */
#define LINPOP_ABSENCEOPT		0x00000100UL	//缺席模式(Member recognition command)
#define LINPOP_SERVEROPT		0x00000200UL	//服务器(保留)
#define LINPOP_DIALUPOPT		0x00010000UL	//发送给个人 Send individual memberrecognition command

#define LINPOP_FILEATTACHOPT		0x00200000UL
#define LINPOP_ENCRYPTOPT		0x00400000UL

/*  option for send command  */
#define LINPOP_SENDCHECKOPT		0x00000100UL	//    要求回信确认
#define LINPOP_SECRETOPT		0x00000200UL
#define LINPOP_BROADCASTOPT		0x00000400UL
#define LINPOP_MULTICASTOPT		0x00000800UL
#define LINPOP_NOPOPUPOPT		0x00001000UL
#define LINPOP_AUTORETOPT		0x00002000UL
#define LINPOP_RETRYOPT			0x00004000UL
#define LINPOP_PASSWORDOPT		0x00008000UL
#define LINPOP_NOLOGOPT			0x00020000UL
#define LINPOP_NEWMUTIOPT		0x00040000UL
#define LINPOP_NOADDLISTOPT		0x00080000UL
#define LINPOP_READCHECKOPT		0x00100000UL
#define LINPOP_SECRETEXOPT		(IPMSG_READCHECKOPT|IPMSG_SECRETOPT)

#define IPMSG_NO_REPLY_OPTS             (IPMSG_BROADCASTOPT|IPMSG_AUTORETOPT)

#endif

